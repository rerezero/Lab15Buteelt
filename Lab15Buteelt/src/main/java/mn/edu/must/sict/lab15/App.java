package mn.edu.must.sict.lab15;

/**
 * Lab15Buteelt төслийн үндсэн утилит класс.
 */
public final class App {

    // Чөлөөт (static) функцуудыг гүйцэтгэхэд зориулсан класс —
    // гаднаас шинэ жишээ үүсэхийг хориглоно.
    private App() {
        // Prevent instantiation
    }

    /**
     * Программын эхлэх цэг.
     *
     * @param args тушаалатын мөрийн өгөгдлүүд.
     */
    public static void main(final String[] args) {
        System.out.println("Hello World!");
    }
}
